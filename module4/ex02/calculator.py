#!/usr/bin/env python3

first_number = int(input("Give me the first number: "))
sec_number = int(input("Give me the second number: "))

print("Thank you!")

print(f"{first_number} + {sec_number} = {first_number + sec_number}")
print(f"{first_number} - {sec_number} = {first_number - sec_number}")
print(f"{first_number} / {sec_number} = {round(first_number / sec_number)}")
print(f"{first_number} * {sec_number} = {first_number * sec_number}")